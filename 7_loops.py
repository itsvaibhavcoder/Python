# For Loop
# Example 1: Iterate over a sequence (e.g., a list)

fruits = ["apple","banana","cherry"]
for fruit in fruits:
    print(fruit)

# Example 2: Using the range function
for i in range(0,5):
    print(i)

# While Loop
# Example 1: Using a while loop to iterate until a condition is False
count = 0
while count<5:
    print(count)
    count+=1

# Example 2: Using a while loop with a break statement

count = 0
while True:
    if count==3:
        break
    print(count)
    count+=1

    
        