'''Comparison operators : comparison operators are used to compare two values and return True and False value
Example : ==, !=, >, <, >=, <, <=
'''
num1 = 3
num2 = 4

print(num1==num2) # False
print(num1!=num2) # True
print(num1>num2) # False
print(num1>=num2) # False
print(num1<num2) # True
print(num1<=num2) # True
